var angka = prompt('Masukkan Angka');

if( angka % 2 == 1 ) {
    alert('Ini Angka Ganjil');
}else if( angka % 2 == 0 ) {
    alert('Ini Angka Genap'); 
}else {
    alert('Yang Anda Masukkan Bukan Angka')
}


